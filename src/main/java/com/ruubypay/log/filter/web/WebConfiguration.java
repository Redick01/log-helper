    @Override
    public void addInterceptors(InterceptorRegistry registry) {
        InterceptorRegistration registration = registry.addInterceptor(new WebHandlerMethodInterceptor());
        try {
            Method method = ReflectUtil.getMethod(InterceptorRegistration.class, "order", Integer.class);
            if (ObjectUtil.isNotNull(method)){
                method.invoke(registration, Ordered.HIGHEST_PRECEDENCE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

